package bean;

public class ImageBean /*이미지업로드시 상속용 자바빈*/ {
	private int no; //번호
	private String image; // 객실 사진 저장본
	
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		image = image;
	}
	
	
}